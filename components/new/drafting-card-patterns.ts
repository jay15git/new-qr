export type DraftingCardPatternId = (typeof DRAFTING_CARD_PATTERNS)[number]["id"]
export type DraftingCardPatternSelectionId =
  | DraftingCardPatternId
  | typeof DRAFTING_CARD_PATTERN_NONE_ID

export function getDraftingCardPatternById(
  patternId: DraftingCardPatternSelectionId,
): DraftingCardPattern | null {
  if (patternId === DRAFTING_CARD_PATTERN_NONE_ID) {
    return null
  }

  return DRAFTING_CARD_PATTERNS.find((pattern) => pattern.id === patternId) ?? null
}

export function getDraftingCardPatternStyle(
  patternId: DraftingCardPatternSelectionId,
  overrides?: DraftingCardPatternColorOverrides,
): DraftingCardPatternStyle | null {
  const pattern = getDraftingCardPatternById(patternId)

  if (!pattern) {
    return null
  }

  const style: DraftingCardPatternStyle = { ...pattern.style }

  for (const slot of pattern.colorSlots) {
    const override = overrides?.[slot.id]

    if (override) {
      style[slot.id] = override
    }
  }

  return style
}
